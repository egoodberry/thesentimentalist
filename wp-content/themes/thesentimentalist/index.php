          <h2>FAQ</h2>

          <div class="grid_6 alpha">
            <section class="questions">
              <p class="question">Do I need an appointment?</p>
              <p class="answer">You are welcome to walk in anytime during the week, but we do require appointments on Saturday and Sunday. Weekend appointments last one hour and are made on the hour.</p>
              <p class="question">Do you sell dresses off the rack?</p>
              <p class="answer">We primarily sell our designer dresses by “special order” and recommend this route, but sometimes our samples can be available for purchase off the rack at a discount.  Our vintage dresses are all sold off the rack.</p>
              <p class="question">How does the special order process work?</p>
              <p class="answer">All of the dresses in our shop that you will be trying on are samples.  We only carry one sample in one size of each of our styles. Once you have selected a dress, we take your measurements and compare them to the designer's size chart. The dresses are made in standard sizes, so we will work with you in choosing the best size for you, but alterations are to be expected.  We then order your specific dress and have it shipped to our shop.</p>
              <p class="question">What if the samples aren’t in my size?</p>
              <p class="answer">We strive to carry samples sizes that will work for most brides, but samples will rarely fit perfectly. We have our own arsenal of tricks and tools to help provide an idea of how the dress will look in your size.</p>
              <p class="question">How long does it take to get my dress?</p>
              <p class="answer">Most designer production time takes about 12 to 14 weeks. After we receive your order, it will take about 5 days to process.  We will provide you with an estimated ship date and keep you updated as that date is refined.</p>
              <p class="question">What about alterations?</p>
              <p class="answer">Alterations are just about always to be expected.  We don’t provide in-house alterations, but we are happy to share a short list of alteration references.</p>
              <p class="question">Can I bring my bridal party?</p>
              <p class="answer">Our little shop is designed to offer an intimate shopping experience, so it’s best to bring a small party. Because our space is limited, we wouldn’t recommend bringing more than 4 people. If you are traveling with a large group or need special assistance, just call ahead of time and let us know.</p>
              <p class="question">Do you have bridesmaids’ dresses?</p>
              <p class="answer">We do not currently carry bridesmaids’ dresses. We will have social dresses visiting and available for special order with certain designers’ trunk shows.</p>
              <p class="question">Do you have a specific dress I am looking for?</p>
              <p class="answer">We only carry a selection of samples from each of the designers we carry, but we can special order any dress from our designers’ available collections. Just call us if you are looking for a specific dress.</p>
              <p class="question">Do you offer any discounts on your dresses?</p>
              <p class="answer">We work hard to offer a range of reasonably priced dresses.  Our dress prices are determined by material costs and the time it makes to create such special garments. Because we respect our designers and want to support their craft, discounts are only extended during sample sales, trunk shows, and other special promotions.</p>
            </section>
