              <hr />
              <h3>Do A Little Research</h3>
              <p>Explore our website a bit and take a look at our designers.</p>
              <h3>Come Prepared</h3>
              <p>Dress comfortably and wear socks if you aren’t comfortable going barefoot.  Also, go light on the makeup, tanner, and jewelry before trying on our pretty white dresses.</p>
              <p>Bring a small but great team. A small, supportive team of family and friends is best for accomplishing a pleasant and productive appointment.</p>
              <h3>Weekday Visiting</h3>
              <p>We recommend dropping in during the week in order to take your time with our dresses and do a little preliminary shopping. We are also happy to have you try on dresses during the week, as it’s often less busy and more relaxed.</p>
              <h3>Keep An Open Mind</h3>
              <p>We always recommend trying on more than less. If anything catches your eye, try it on! You will learn more about what you really want by trying on several styles and keeping an open mind. The dress you fall in love with might not be what you had in mind, and that only makes it extra special!</p>
